#include "../header/ft_head.h"

int		ft_atoi(char *str)
{
	int res;

	if (*str == 0)
		return (0);
	res = 0;
	while (*str)
	{
		res = res * 10 + (*str - '0');
		str++;
	}
	return (res);
}
